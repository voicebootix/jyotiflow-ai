async function tableExists(tableName) {
  const result = await client.query(
    'SELECT EXISTS (SELECT FROM information_schema.tables WHERE table_name = $1)',
    [tableName]
  );
  return result.rows[0].exists;
}

async function columnExists(tableName, columnName) {
  const result = await client.query(
    'SELECT EXISTS (SELECT FROM information_schema.columns WHERE table_name = $1 AND column_name = $2)',
    [tableName, columnName]
  );
  return result.rows[0].exists;
}

async function createTable(tableName, schema) {
  const columns = Object.entries(schema.columns)
    .map(([name, def]) => `${quoteIdentifier(name)} ${def.type}`)
    .join(', ');
  
  try {
    await client.query(`CREATE TABLE ${quoteIdentifier(tableName)} (${columns})`);
    console.log(`  âœ… Created table: ${tableName}`);
  } catch (error) {
    console.log(`  âš ï¸  Failed to create table ${tableName}: ${error.message}`);
  }
}

